    """Calculate Critical Success Index"""
    cm = confusion_matrix(y_true, y_pred)
    tn, fp, fn, tp = cm.ravel()
    csi = tp / (tp + fn + fp) if (tp + fn + fp) > 0 else 0
    return csi

