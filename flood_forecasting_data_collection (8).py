# Features to impute (check what exists in df)
features_to_impute = []

# Check for optical features
for feat in ['ndwi', 'mndwi', 'ndvi', 'red', 'nir', 'green']:
    if feat in df.columns:
        features_to_impute.append(feat)

# Check for SAR features
